MSG2 DW 'IN UPPERCASE: $'  
CHAR DB ?
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
     
    LEA DX,MSG1
    MOV AH,9
    INT 21H  
    
    MOV AH,1 
    INT 21H    
    SUB AL,20H
    MOV CHAR,AL
    
    MOV AH,2
    MOV DX,0DH
    INT 21H
    MOV DX,0AH
    INT 21H
             
    LEA DX,MSG2 
    MOV AH,9
    INT 21H
    
    MOV AH,2
    MOV DL,CHAR
    INT 21H
    
    MAIN ENDP